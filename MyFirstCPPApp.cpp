#include <iostream>
using namespace std;

int main()
{
    // ASCII -- American Standard Code for Information Interchange
    cout << (int)'a' << endl;   // Casting operator
    cout << int('a') << endl;   // Output 97
    cout << int('A') << endl;   // Output 65
    cout << char(65) << endl;   // Output A

    system("pause>0");
}

